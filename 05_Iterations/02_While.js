// while

let index = 0 // starting from
while (index <= 10) { // condition till end point
    // console.log(`Value of index is ${index}`);
    index = index + 2 // increment order
}

// while loop in arrays
let myArray = ["a", "b", "c"]
let arr = 0
while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1
}

// do while
let score = 10
do {
    // console.log(`Score is ${score}`);
    score = score + 10
} while (score <= 100);