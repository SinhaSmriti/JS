// simple comparisons
console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// string value comparison to number
console.log("2" > 1);
console.log("02" > 1);

// null value comparison
console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

// undefined comparison
console.log(undefined == 0);
console.log(undefined > 0);
console.log(undefined < 0);

// === // for comparing datatypes

console.log("2" === 2);